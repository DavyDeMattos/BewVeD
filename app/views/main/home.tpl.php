    <h2>Bienvenue sur BewVeD</h2>
    <div>
      <p>Le centre de formation « BewVeD » souhaite avoir une interface afin de faire des groupes automatiques sur certains critères tels que l’âge, sexe et acquis pour des sessions de 10 à 25 apprenants.
      L’application devra permettre à l’utilisateur de saisir manuellement ou d’importer les informations (via un CSV) sur ces apprenants. 
      Voici les différentes informations pour chaque apprenant : <strong>Promotion, Nom, Prénom, Sexe, Age, Compétences</strong>.
      Il faudra donc établir une gestion avec une base de données consultable et éditable par les formateurs.
      Afin que les groupes soient formé, le formateur devra renseigner plusieurs éléments :
      <ol>
        <li>Le nombre de membres d’un groupe.</li>
        <li>La promotion concernée via un nom ou un code.</li>
        <li>Les filtres souhaités pour les associations.</li>
      </ol>
      L’objectif est que les groupes soient hétéroclites en fonction d’un ou plusieurs filtres. Pour la composition d’un groupe, il faut qu’il soit :
      <ul>
        <li>Mixte grâce au filtre « Sexe ».</li>
        <li>Composé de personnes ayant des tranches âges différentes pour qu’il soit générationnel grâce au filtre « Age ».</li>
        <li>Composé de personnes ayant des compétences complémentaires en fonction du filtre « Compétences ». Les compétences pourront être catégorisé afin de pouvoir définir la complémentarité des groupes.</li>
      </ul>
      Tous les langages sont autorisés tant qu’ils sont compatibles avec les navigateurs actuels dans leurs dernières versions. 
      Une fois les filtres et le nombre de membres par groupe défini, l’application devra afficher un résultat des groupes formé avec le nom et prénom de ses membres.
      </p>
